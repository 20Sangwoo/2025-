package day5;

public class Array_3 {
  public static void main(String[] args) {
    boolean[] flag;
    flag = new boolean[3];

    for(int i=0; i<flag.length; i++) {
      System.out.println(flag[i]);
    }

    String[] name;
    name = new String[3];

    for(int i=0; i<name.length; i++) {
      System.out.println(name[i]);
    }
  }
}
